#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*str;

	i = 0;
	if (!s)
		return ((void *) 0);
	str = (char *)malloc(sizeof(char) * ft_strlen(s) + 1);
	if (str == (void *) 0 || !s)
		return ((void *) 0);
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
